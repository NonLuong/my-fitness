export function resolveExerciseKeyFromLabel(label: string): ExerciseKey | null {
  for (const rule of EXERCISE_KEY_BY_LABEL) {
    if (rule.match.test(label)) return rule.key;
  }
  return null;
}

export function resolveExerciseDetailFromLabel(label: string): ExerciseDetail | null {
  const key = resolveExerciseKeyFromLabel(label);
  if (!key) return null;
  return EXERCISE_DETAILS[key] ?? null;
}
